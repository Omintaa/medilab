// JavaScript functions for hover effect
var heartInfo = document.querySelector('.heartinfo');
var icon = document.querySelector('.heartinfo i.fa');
var textElements = document.querySelectorAll('.heartinfo .hearth3, .heartinfo .heartp');

heartInfo.addEventListener('mouseover', function() {
  // Change styles on hover
  heartInfo.style.backgroundColor = '#3498db';
  icon.style.backgroundColor = 'white';
  icon.style.color = '#3498db';
  textElements.forEach(function(element) {
    element.style.color = 'white';
  });
});

heartInfo.addEventListener('mouseout', function() {
  // Reset styles on mouseout
 heartInfo.style.backgroundColor = 'transparent';
  icon.style.backgroundColor = 'rgb(25, 121, 204)';
  icon.style.color = 'white';
  textElements.forEach(function(element) {
    element.style.color = 'rgb(17, 83, 141)';
  });
});


